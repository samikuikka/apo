/**
 * Bundles the agent-task runtime into a deployable ESM bundle (SPEC-125).
 *
 * Output layout:
 *
 *   dist/agent-task-runtime/
 *     runner.mjs           ← entry, launched via `node runner.mjs`
 *     package.json         ← marks the folder as a module
 *
 * The backend image copies this folder to /app/agent-task-runtime so the
 * backend can run agent tasks without depending on the repo's dev-only
 * `tsx` install or live TypeScript source imports.
 */

import { build } from "esbuild";
import { mkdir, rm, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const sdkRoot = resolve(here, "..");
const outDir = resolve(sdkRoot, "dist", "agent-task-runtime");
const entry = resolve(sdkRoot, "src", "agent-task", "runner-entry.ts");

await rm(outDir, { recursive: true, force: true });
await mkdir(outDir, { recursive: true });

/** @type {import("esbuild").BuildOptions} */
const options = {
  entryPoints: [entry],
  bundle: true,
  format: "esm",
  platform: "node",
  target: "node20",
  outfile: resolve(outDir, "runner.mjs"),
  sourcemap: true,
  // Respect Node's resolution so runtime peer deps (openai, p-limit, etc.)
  // are externalised and resolved from the deployed node_modules.
  external: [
    "openai",
    "p-limit",
    "unique-names-generator",
    "effect",
    "@effect/platform",
  ],
  banner: {
    js: [
      "// Auto-generated by packages/sdk/scripts/build-agent-task-runtime.mjs.",
      "// Do not edit by hand — regenerate via `pnpm --filter sdk build:agent-task-runtime`.",
    ].join("\n"),
  },
  logLevel: "info",
};

await build(options);

// ESM modules need a sibling package.json so `node runner.mjs` treats
// the folder correctly when resolved from anywhere on disk.
await writeFile(
  resolve(outDir, "package.json"),
  JSON.stringify(
    {
      name: "apo-agent-task-runtime",
      version: "0.0.0",
      private: true,
      type: "module",
      main: "runner.mjs",
    },
    null,
    2,
  ) + "\n",
);

console.log(`agent-task runtime bundle written to ${outDir}`);
